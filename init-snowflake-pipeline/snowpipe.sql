SELECT * FROM FRANCE_EMPLOI_DB.STAGING.OFFRES_EMPLOI_FRANCE_TRAVAIL_RAW  LIMIT 10;
SELECT * FROM FRANCE_EMPLOI_DB.STAGING.TRANCHE_AGE_RAW                   LIMIT 10;
